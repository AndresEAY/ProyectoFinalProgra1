    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        profesor.nombre = jTextField1.getText();
        profesor.apellido = jTextField2.getText();
        profesor.usuario = jTextField3.getText();
        profesor.password = jTextField4.getText();

        if (VerificarContrasenia.verify(profesor.password)) {
            LoginA.profesores.set(posicion, profesor);

            JOptionPane.showMessageDialog(this, "Profesor actualizado con éxito");

            ConsultaProfesor c = new ConsultaProfesor();
            c.setVisible(true);
            this.dispose();
        } else {
            JOptionPane.showMessageDialog(this, "La contraseña no cumple con los requisitos");
        }
    }//GEN-LAST:event_jButton1ActionPerformed


