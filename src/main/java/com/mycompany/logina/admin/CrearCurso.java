    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SimpleDateFormat formatoFecha = new SimpleDateFormat("dd/MM/yyyy");
        SimpleDateFormat formatoHora = new SimpleDateFormat("HH:mm");

        Curso c = new Curso();
        try {
            c.setIdCurso(jTextField1.getText());
            c.setNombre(jTextField2.getText());
            c.setSeccion(jTextField8.getText());
            c.fechaInicio = formatoFecha.parse(jTextField3.getText());
            c.fechaFin = formatoFecha.parse(jTextField4.getText());
            c.horaInicio = formatoHora.parse(jTextField5.getText());
            c.horaFin = formatoHora.parse(jTextField6.getText());
            c.setProfesor(jComboBox1.getSelectedItem().toString());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Alguno de los campos es incorrecto o vacío\n"
                    + "Formatos permitidos:\n"
                    + "Fecha: dd/MM/yyyy\n"
                    + "Hora: HH:mm");
        }
        
        try{
            LoginA.cursos.add(c);
            LoginA.profesores.get(jComboBox1.getSelectedIndex()-1).cursos.add(c);

            jTextField1.setText("");
            jTextField2.setText("");
            jTextField3.setText("");
            jTextField4.setText("");
            jTextField4.setText("");
            jTextField5.setText("");
            jTextField6.setText("");
            jTextField8.setText("");

            String id = GenerarId.generarId();
            jTextField1.setText(id);

            actualizarJComboBox();

            JOptionPane.showMessageDialog(this, "Curso agregado con éxito");
        } catch(Exception e){
            System.out.println(e);
            JOptionPane.showMessageDialog(this, "Error al guardar los datos");
        }

    }//GEN-LAST:event_jButton1ActionPerformed

