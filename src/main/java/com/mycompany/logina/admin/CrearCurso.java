    private void actualizarJComboBox(){
        DefaultComboBoxModel<String> profesores = new DefaultComboBoxModel<>();
        profesores.addElement("");
        for (Profesor profesor : LoginA.profesores) {
            profesores.addElement(profesor.usuario);
        }
        jComboBox1.setModel(profesores);
    }
